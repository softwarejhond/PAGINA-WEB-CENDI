						General de Educación (115/94) y su Decreto Reglamentario No. 4904 de 16 de 
						Diciembre de 2009.
						<br><br>
						
						Somos líderes en la convivencia en paz y en la formación de alumnos competentes 
						en el desarrollo humano y en el conocimiento técnico desarrollado por competencias 
						laborales.
						<br><br>
						La técnica desarrollada por competencias laborales cumple un papel importante 
						en la industria nacional ya que promueven fundamentalmente el desarrollo social, 
						económico y cultural. Acordes con el objetivo del Ministerio de Educación Nacional 
						por el mejoramiento de la calidad de la educación y la formación integral para el 
						trabajo, se debe buscar lograr esta meta mediante la implementación de nuevos currículos
						 pertinentes a la situación actual y laboral de nuestro medio y buscando el desarrollo 
						 integral de los educandos en sus habilidades, destrezas, aptitudes, conocimientos e 
						 intereses, para alcanzar un trabajo productivo, digno y estable.
						
						Este proceso conlleva la planeación de un diseño curricular orientado a la formación por competencias que consignan las mesas sectoriales que lidera el SENA y en la consulta continua de sus mesas sectoriales para determinar las necesidades que se presentan en el sector empresarial y hacia allá encaminar los esfuerzos y la orientación para proporcionar al educando mayores y mejores posibilidades de empleo sostenible que lo beneficien y dignifiquen su forma de vida.</p>
					
